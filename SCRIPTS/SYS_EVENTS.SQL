COL EVENT FORMAT A30
COL TOTAL_WAITS FORMAT 999999
COL TOTAL_TIMEOUTS FORMAT 999999
COL TIME_WAITED FORMAT 999999
COL AVERAGE_WAIT FORMAT 999999
COL TIME_WAITED_MICRO FORMAT 999999

SELECT 
EVENT,
TOTAL_WAITS,
TOTAL_TIMEOUTS,
TIME_WAITED,
AVERAGE_WAIT,
TIME_WAITED_MICRO
FROM V$SYSTEM_EVENT
ORDER BY  TOTAL_TIMEOUTS DESC
/
